#!/usr/bin/env bash

# Скрипт для проверки, является ли число простым

is_prime() {
    local num=$1

    # Проверка на числа меньше 2
    if (( num <= 2 )); then
        echo "$num является простым числом."
        return
    fi

    # Проверка делителей от 2 до квадратного корня из num
    for (( i=2; i*i<=num; i++ )); do
        if (( num % i == 0 )); then
            echo "$num не является простым числом."
            return
        fi
    done

    echo "$num является простым числом."
}

# Запрос числа у пользователя
read -p "Введите число: " number

# Проверка на целое число
if ! [[ "$number" =~ ^[0-9]+$ ]]; then
    echo "Пожалуйста, введите целое неотрицательное число."
    exit 1
fi

# Вызов функции для проверки числа на простоту
is_prime $number