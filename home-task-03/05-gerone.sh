#!/bin/bash

# Проверка на количество аргументов
if [ "$#" -ne 3 ]; then
    echo "Использование: $0 <сторона1> <сторона2> <сторона3>"
    exit 1
fi

# Присваиваем аргументы переменным
a=$1
b=$2
c=$3

# Проверка на положительность сторон
if (( $(echo "$a <= 0" | bc -l) || $(echo "$b <= 0" | bc -l) || $(echo "$c <= 0" | bc -l) )); then
    echo "Стороны треугольника должны быть положительными числами."
    exit 1
fi

# Вычисление полупериметра
s=$(echo "scale=1; ($a + $b + $c) / 2" | bc)

# Вычисление площади по формуле Герона
area=$(echo "scale=1; sqrt($s * ($s - $a) * ($s - $b) * ($s - $c))" | bc -l)

# Вывод результата
echo "Площадь треугольника: $area"